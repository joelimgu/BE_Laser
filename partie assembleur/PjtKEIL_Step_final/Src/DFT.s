Xr proc
	push {r4,r5,r6,r7}
	; r0 = signal[], r1 = k
	mov r2,#0 ; int i = 0
	mov r3,#0 ; sum = 0 dans r3
start_cos
	;cmp r2,#64 
	;beq end_for
	ldrsh r4,[r0,r2,LSL #1] ; r4 = signal[i]  ; s pour préserver le signe 
	mul r5,r2,r1 ; r5 = p = k*n ; int*int OK
	; cos
	and r5,r5,#0x003F ; use only 6 bits so to make mod 64
	ldr r6,=TabCos
	ldrsh r7,[r6,r5, LSL #1] ; r7 = TabCos[p%64]  ; s pour préserver le signe 
	; end cos
	mul r7,r7,r4 ; r7 = cos*signal  ; 1:15 * 4:12  -> 5:27
	add r3,r7 ; sum += cos*signal ; 5:27 + 5:27
	add r2, #1
	cmp r2,#64 
	bne start_cos
; end_for
	mov r0,r3 ; résultat en format 5:27!!
	pop {r7,r6,r5,r4}
	bx lr 
	endp

	
Xi proc
	push {r4,r5,r6,r7}
	; r0 = signal[], r1 = k
	mov r2,#0 ; int i = 0
	mov r3,#0 ; sum = 0 dans r3
start_sin
	;cmp r2,#64 
	;beq end_for
	ldrsh r4,[r0,r2,LSL #1] ; r4 = signal[i]  ; s pour préserver le signe 
	mul r5,r2,r1 ; r5 = p = k*n ; int*int OK
	; cos
	and r5,r5,#0x003F ; use only 6 bits so to make mod 64
	ldr r6,=TabSin
	ldrsh r7,[r6,r5, LSL #1] ; r7 = TabSin[p%64]  ; s pour préserver le signe 
	; end cos
	mul r7,r7,r4 ; r7 = sin*signal  ; 1:15 * 4:12  -> 5:27
	add r3,r7 ; sum += sin*signal ; 5:27 + 5:27
	add r2, #1
	cmp r2,#64 
	bne start_sin
; end_for
	mov r0,r3 ; résultat en format 5:27!!
	pop {r7,r6,r5,r4}
	bx lr 
	endp


DFT proc
	; r0->&signal r1->k
	push {lr,r4,r5,r6,r7,r8,r9}
	; on protege les arguments
	mov r6, r0
	mov r7, r1
	
	bl Xr
	mov r4,r0 ; on gqrde le resultat de Xr
	; on remet les arguments pour l,apel a Xi
	mov r0, r6
	mov r1, r7
	bl Xi
	mov r8, r0 ; on gqrde le resultqt de Xi
	
	smull r4,r5,r4,r4 ; 10:54 ; multiplication sur 64 bits, on garde dans r5 les poids forts et dans r4 les poids faibles
	smull r8,r9,r8,r8 ; 10:54
	add r0,r5,r9 ; on prend juste les 32 bits de poids fort et on a un format 10:22
	
	pop {r9,r8,r7,r6,r5,r4}
	pop {pc}
	endp

